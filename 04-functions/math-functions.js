// Demonstrating top 6 Math functions in JavaScript

// 1. Math.abs() - Returns the absolute value
console.log("Math.abs(-7):", Math.abs(-7)); // Output: 7

// 2. Math.round() - Rounds to the nearest integer
console.log("Math.round(4.6):", Math.round(4.6)); // Output: 5

// 3. Math.floor() - Rounds down to the nearest integer
console.log("Math.floor(4.9):", Math.floor(4.9)); // Output: 4

// 4. Math.ceil() - Rounds up to the nearest integer
console.log("Math.ceil(4.1):", Math.ceil(4.1)); // Output: 5

// 5. Math.max() - Returns the largest of zero or more numbers
console.log("Math.max(3, 7, 2):", Math.max(3, 7, 2)); // Output: 7

// 6. Math.random() - Returns a pseudo-random number between 0 (inclusive) and 1 (exclusive)
console.log("Math.random():", Math.random()); // Output: random number between 0 and 1